inline int discretize_MN(int MN) {
    if (MN <= 16) {
        return 16;
    } else if (MN <= 32) {
        return 32;
    } else if (MN <= 64) {
        return 64;
    } else if (MN <= 128) {
        return 128;
    } else if (MN <= 256) {
        return 256;
    } else if (MN <= 512) {
        return 512;
    } else if (MN <= 1024) {
        return 1024;
    } else if (MN <= (1024 + 2048) / 2) {
        return 1024;
    } else if (MN <= (2048 + 3584) / 2) {
        return 2048;
    } else if (MN <= (3584 + 4096) / 2) {
        return 3584;
    } else if (MN <= (4096 + 8192) / 2) {
        return 4096;
    } else if (MN <= (7168 + 8192) / 2) {
        return 7168;
    } else if (MN <= (8192 + 16384) / 2) {
        return 8192;
    } else {
        return 16384;
    }
}

inline int discretize_K(int K) {
    if (K < 2048 - 128) {
        return 1024;
    } else if (K < (2048 + 4096) / 2) {
        return 2048;
    } else if (K < (4096 + 8192) / 2) {
        return 4096;
    } else if (K == 7392) {
        return 7392;
    } else if (K < (7168 + 8192) / 2) {
        return 7168;
    } else if (K < (8192 + 16384) / 2) {
        return 8192;
    } else {
        return 16384;
    }
}

