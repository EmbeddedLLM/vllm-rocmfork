struct TripletHasher {
    size_t operator()(const std::tuple<int, int, int> &input) const {
        return (((std::hash<int>()(std::get<0>(input))) ^
                 (std::hash<int>()(std::get<1>(input)) << 1)) >> 1) ^
               (std::hash<int>()(std::get<2>(input)) << 1);
    }
};

